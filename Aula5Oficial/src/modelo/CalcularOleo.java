package modelo;

public class CalcularOleo {
	
		public float frascoML (float valor, float quantidade) {
			float valorTotal = valor*quantidade;
			return valorTotal;
		}
		
		public float frascoL (float valor, float quant) {
			float valorTotal = valor*quant;
			return valorTotal;
		}
		
		public float total (float val, float val2) {
			float resp = val + val2;
			return resp;
		}

	}